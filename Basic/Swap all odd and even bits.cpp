class Solution
{
    public:
    //Function to swap odd and even bits.
    unsigned int swapBits(unsigned int n)
    {
    	// Your code here
        int sinistra = (n & 0XAAAAAAAA);
        int destra   = (n & 0X55555555);
        sinistra = sinistra >> 1;
        destra = destra << 1;
        
        return sinistra | destra; 
    }
};

